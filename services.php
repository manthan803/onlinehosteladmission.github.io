 <h1><i><u>CHILD CARE :</u></i></h1>
		<P> The developing economies like India are now empowering women for financial and social enhancement through employment opportunities. Working women irrespective of staying in nuclear or joint families require a safe and educative growing environment for their children. Mother Theresa Child Care centre at Sathyabama Institute of Science and Technology is an exclusive initiative ensuring a home away from home for the children of the SATHAYABAMA employers.</P>

    <P> The Centre is the vision of Dr. Marie Johnson, Director Sathyabama Institute of Science and Technology who strongly believes that the key for effective contribution in the work environment is the requirement of undisturbed routine and peace in the day to day activities. Children, being the priority of concern to the parents, are highly influential in their professional advancement. Under the guidance of Dr. Mariazeena Johnson, a survey had been conducted and every detail of requirement by the parents and the children has been enumerated. The Mother Theresa Day Care centre is thus an organized facility in which the essentials for child care are carefully incorporated in a novel and attractive manner.</P>

    <P>The Day care centre has wide rooms for playing and 3 special rooms for sleeping for the kids. The entire area is plastered with 3 feet of rubber walls to ensure the safety of the little children. Presently provisions have been provided to cater to the children from the age of 6months to 3.5 yrs of age. Special kitchen for preparing food for the children and equipped toilets with clean and hygienic usage are the highlights.</P>

     <P>A Trained Montessori teacher is appointed to enable education in grooming such as manners at home, manners in public, table manners and toilet habits. Developmental concepts of numbers, time, colors, forms, words and creativity are designed to be implemented for the required children. Colorful toys and several educative blocks to develop motor and cognitive skills of the children are provided and the children are taught according to their choices and likes.</P>

       <P>The centre was inaugurated on the 18th of March 2013 and the parents are overwhelmed with the safe and convenient atmosphere and learning environment provided beyond comparison well within their reach. Currently 20 children have been enrolled and parents of children above 5years of age have urged the management to facilitate a similar learning platform for their wards during their holidays. The management has been working on the further developments such as providing special visual provisions to enable parents to see their children at the centre from their departments. Club activities to facilitate activity- based fun learning are being devised for children aged above 5 years.</P>

      <P>  The Mother Theresa Day Care Centre is considered a boon by the employees of Sathyabama Institute of Science and Technology and this measure to enable independent and stress free work environment is lauded as the best initiative of women empowerment.  </P>

 <h1><I><u>MINARAL WATER FACILITY :</u></I></h1>
      <P>The Reverse Osmosis Plant installed in the Satyabama Campus, caters to the drinking water needs of all the Students, Teachers, supporting Staff and the Visitors. The raw water with an average Total Dissolved Solids [TDS] of 750-1000 ppm is treated to reduce the TDS content to less than 100 ppm the generally acceptable upper limit of the TDS

      <P>This is done in Three stages, namely: Pretreatment, passage of the water through 6 RO membrane filters held in parallel to each other{main process] and Post treatment. The maximum capacity of the plant is 1500 litres per hour [at the rate of 250 lit per membrane] The utilized capacity is 1000 lit/hr potable water. This water is Pure, Tasty and Hygienic rendering it highly suitable for human consumption.</P>

      <P>The rejects water from the plant is used for gardening and washing purposes. This plant not only facilitates the supply of safe drinking water but also obviates the otherwise involved huge expenditure for procuring drinking water for the large number of people in the campus. Now the institution can be liberal in the provision of our own made RO water.</P>

      <P> 66 coolers are connected to this plant providing drinking water in every floor of class room blocks, administrative block, library and  laboratories Etc..</P>

