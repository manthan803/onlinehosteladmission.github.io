    <h1 class='head2'><b><u>INTERNAL MANAGEMENT</b></u></h1>
    <div id='about2'>
        <p>- The internal management of the hostel rests with the Superintendent and Assistant Superintendent of the hostel.
        </p>
        <br>

        <p>- Boarders should sign daily in the roll call register supplied by the Superintendent at 7.30 P.M. in their respective
         rooms. The Register will be circulated by a boarder. The Boarders have to intimate about their problems/guests / application
          for leave, if any, in the columns specified in the register with separate applications.
        </p>
        <br>

        <p>- Boarders should always keep Hostel Identity Cards with them. Boarders should return to the hostel at least by
         6.00 P.M. After 6.00 P.M. the gate shall be closed and if one comes late he/she should sign the gate book with 
         reasons of being late and time of arrival. Continuous late arrival may be intimated to the Principal / Parents/Guardians for necessary action.
         </p>
         <br>

        <p>- The study hour will be observed strictly both in the morning and evening hours. No one is allowed to move from one room to the other or disturb
         anybody in any manner specially during the study hours.
        </p>
        <br>

        <p>- Study hour may not be observed on Saturday and on nights preceding holidays, except in the evening preceding the reopening of the College after 
        the vacation and holidays. However, this does not permit any boarder to disturb other boarders who wish to read during such periods.
        </p>
        <br>

        <p>- Time hours of lunch and dinner are to be fixed by the Superintendent and boarders are required to follow is strictly.
        </p>
        <br>

        <p>- Inmates of a room must ensure that fan and light points are put off when no one is present in the room. The room lights are to be put off by 11 P.M.
        </p>
        <br>

        <p>- The Superintendent will maintain a conduct register of the boarders in which the records of all fines and punishment will be maintained. Gross
         misconduct of a boarder shall also render him to have his name entered in the conduct register of the college.
        </p>
        <br>

        <p>- A boarder may be removed from the hostel by the Principal on the report of the Superintendent for unmannerly and indecent behaviour towards fellow 
        boarders & hostel employees, breach of discipline or incitement to breach of discipline & gross misconduct.
        </p>
        <br>

        <p>- The Medical Officer visits the hostel twice a week. In case of illness boarders are required to make necessary entries in the Medical Register which
         will be submitted to the Medical Officer for prescription & treatment. In case of serious illness the boarders must inform the Superintendent forthwith
          who will make necessary arrangement for his / her treatment.
        </p>
        <br>

        <p>- Father or guardian of a boarder may be permitted only after previous sanction of the Superintendent to reside in the guest room of the hostel for one 
        night, provided that the visit is related to the interest or welfare of the boarder as a pupil of the College & provided that accommodation is available.
         They are not supposed to remain with their sons/wards in their rooms.
        </p>
        <br>

        <p>- There shall be a Residence Committee comprising the Principal, Superintendents and Assistant Superintendents of the hostels and two staff members nominated 
        by the Principal. The committee shall discuss all matters relating to the smooth conduct of the hostels and their administration.
        </p>
        <br>
    
    </div>

